print 6 & 5; // 4
print "\n";
print 6 | 5; // 7
print "\n";
print 6 ^ 5; // 3
print "\n";
print 6 << 5; // 192 [6 * (2*2*2*2*2) == 32 | 6 * 32 == 192]
print "\n";
print 6 >> 5; // 0   [6 / (2*2*2*2*2) == 32 | 6 / 32 == 0]